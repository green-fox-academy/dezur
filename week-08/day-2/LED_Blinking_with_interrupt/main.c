#include "stm32f7xx.h"
#include "stm32746g_discovery.h"

static void SystemClock_Config(void);

TIM_HandleTypeDef timer_handle;
GPIO_InitTypeDef PA15_LED_config;

void init_timer()
{
    __HAL_RCC_TIM2_CLK_ENABLE();

    timer_handle.Instance = TIM2;
    timer_handle.Init.Prescaler = 54000 - 1; 
    timer_handle.Init.Period = 1000 - 1;
    timer_handle.Init.ClockDivision = TIM_CLOCKDIVISION_DIV1;
    timer_handle.Init.CounterMode = TIM_COUNTERMODE_UP;

    /* configuring the timer in PWM mode instead of calling HAL_TIM_Base_Init(&timer_handle) */
    HAL_TIM_Base_Init(&timer_handle);

    /* assign the highest priority to our interrupt line */
    HAL_NVIC_SetPriority(TIM2_IRQn, 1, 0);

    /* tell the interrupt handling unit to process our interrupts */
    HAL_NVIC_EnableIRQ(TIM2_IRQn);
}

void init_led_pins()
{
    __HAL_RCC_GPIOA_CLK_ENABLE();

    PA15_LED_config.Pin = GPIO_PIN_15;
    PA15_LED_config.Mode = GPIO_MODE_OUTPUT_PP; /* configure as output, in PUSH-PULL mode */
    PA15_LED_config.Pull = GPIO_NOPULL;
    PA15_LED_config.Speed = GPIO_SPEED_HIGH;

    HAL_GPIO_Init(GPIOA, &PA15_LED_config);
}


int main(void)
{
    HAL_Init();

    /* this function call sets the timers input clock to 108 Mhz (=108000000 Hz) */	
    SystemClock_Config();

    init_led_pins();
    init_timer();

    /* starting the timer */
    HAL_TIM_Base_Start_IT(&timer_handle);

    while (1) {
	    
        }
        
}

void TIM2_IRQHandler(void)
{
    HAL_TIM_IRQHandler(&timer_handle);
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
    if (htim->Instance == TIM2) {
        /* toggling the LED once in every second */
        HAL_GPIO_TogglePin(GPIOA, GPIO_PIN_15);
    }
}

